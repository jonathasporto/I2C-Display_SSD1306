void process_and_split_array(unsigned int input[], unsigned int output[8][8])
{
    // Substitui 0x00000000 por 0 e 0xFF000000 por 1
    for (int i = 0; i < 64; i++)
    {
        if (input[i] == 0x00000000)
        {
            output[i / 8][i % 8] = 0; // Coloca 0 em sua posição
        }
        else if (input[i] == 0xFF000000)
        {
            output[i / 8][i % 8] = 1; // Coloca 1 em sua posição
        }
    }
}

void convert_to_hex(unsigned int output[8][8], unsigned int hex_array[8])
{
    for (int i = 0; i < 8; i++)
    {
        unsigned int hex_value = 0;
        for (int j = 0; j < 8; j++)
        {
            hex_value |= (output[i][j] << (7 - j)); // Converte 8 bits para um número
        }
        hex_array[i] = hex_value; // Armazena o valor hexadecimal
    }
}

void print_hex_array(unsigned int hex_array[8])
{
    for (int i = 0; i < 8; i++)
    {
        printf("0x%02X", hex_array[i]); // Formato de 2 dígitos hexadecimais
        if (i < 7)
        {
            printf(", "); // Adiciona vírgula entre os elementos
        }
    }
    printf("\n");
}

